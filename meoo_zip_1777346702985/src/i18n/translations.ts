// Map all other languages to English as fallback
const supportedLangs = ['en', 'zh', 'ja', 'fr', 'es'];

export const getTranslation = (lang: string, key: string) => {
  // If lang is not supported, fallback to English
  const effectiveLang = supportedLangs.includes(lang) ? lang : 'en';
  
  const keys = key.split('.');
  let result = translations[effectiveLang];
  for (const k of keys) {
    result = result?.[k];
  }
  return result || key;
};
